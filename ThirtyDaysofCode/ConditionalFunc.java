


public class ConditionalFunc {
  
  public static void main(String[] args) {
    
    // middle value beside the condition will be selected if the function passes as true, but if not the one to the right will be selected
    String a = ((7 > 3) ? "yas" : "moron no"); // A conditional function
                                              // This sign (?) is called a conditional operator
    System.out.println(a);

  }
}
